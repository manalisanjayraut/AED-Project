    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        int selectedRow = jtablemed.getSelectedRow();
        if (selectedRow >= 0) {
            Object selectedObject = jtablemed.getValueAt(selectedRow, 0);
             Req_Emergency request = null;
             if (selectedObject instanceof Req_Emergency) {
                 request = (Req_Emergency) selectedObject;
                if(request.getStatus()!= null && request.getStatus().equals("Completed")){
                     JOptionPane.showMessageDialog(null, "Selected item is already completed.");
                     return ;
                }
             }
            if (selectedObject instanceof Req_Emergency) {
                request = (Req_Emergency) selectedObject;
                
                request.setStatus("Completed");
                
                JOptionPane.showMessageDialog(null, "Request processed successfully.");
            } else {
                JOptionPane.showMessageDialog(null, "Selected item is not a Request Emergency.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a request message to process.");
        }

    }//GEN-LAST:event_btnProcessActionPerformed

