    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jtablemed.getModel();

        model.setRowCount(0);
        System.out.println("emergencyOrganization.getReqEmgDir().getEmergencyUserList()" + emergencyOrganization);
        for (Req_Emergency request : emergencyOrganization.getReqEmgDir().getEmergencyUserList()) {
            Emergency requestEmg = request.getEmergencyID(); // Get the Lab associated with the request
            // System.out.println("requestLab: " + requestLab);
            //  System.out.println("user name is****" + userAccount.getUsername());
            if (requestEmg != null && requestEmg.e.getUserName().equals(userAccount.getUsername())) {

                //System.out.println("request: " + request);
                Object[] row = new Object[5];
                row[0] = request; // Storing the object reference in row[0]
                row[1] = request.getUserId().getUsername(); // Displayed in the table
                row[2] = request.getLocation(); // Displayed in the table
                row[3] = request.getStatus();

// Add row to the table model
                  model.addRow(row);

            }
        }

    }


